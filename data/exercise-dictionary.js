// ── Index + helpers ─────────────────────────────────────────────────

const _normExercise = (s) =>
  (s || '')
    .toString()
    .toLowerCase()
    .normalize('NFD')
    .replace(/[̀-ͯ]/g, '')
    .replace(/[-_/]/g, ' ')
    .replace(/[^a-z0-9\s]/g, ' ')
    .replace(/\s+/g, ' ')
    .trim()

const _DICT_INDEX = (() => {
  const map = new Map()
  for (const entry of EXERCISE_DICTIONARY) {
    const keys = new Set()
    keys.add(_normExercise(entry.es))
    keys.add(_normExercise(entry.en))
    for (const a of entry.aliases || []) keys.add(_normExercise(a))
    for (const k of keys) {
      if (!k) continue
      if (map.has(k)) {
        const existing = map.get(k)
        if (existing.id !== entry.id) {
          console.warn(`[exercise-dictionary] alias collision: "${k}" already in "${existing.id}", ignoring duplicate from "${entry.id}"`)
        }
        continue
      }
      map.set(k, entry)
    }
  }
  return map
})()

const _DICT_BY_ID = (() => {
  const map = new Map()
  for (const entry of EXERCISE_DICTIONARY) {
    map.set(entry.id, entry)
  }
  return map
})()

function normalizeExerciseName(s) {
  return _normExercise(s)
}

function findExerciseEntry(name) {
  const key = _normExercise(name)
  if (!key) return null
  return _DICT_INDEX.get(key) || null
}

// dictId format: "dict_" + dictionary entry.id (e.g. "dict_press-banca-barra")
function getEntryById(dictId) {
  if (!dictId || !dictId.startsWith('dict_')) return null
  return _DICT_BY_ID.get(dictId.slice('dict_'.length)) || null
}

// Token-based fuzzy match across the local dictionary (offline).
// Score: 1.0 exact normalized match; 0.8+ substring inclusion weighted by length;
// (0..0.8) by token overlap ratio. Threshold 0.5 by default.
function findExerciseEntryFuzzy(name, threshold = 0.5) {
  const exact = findExerciseEntry(name)
  if (exact) return exact

  const q = _normExercise(name)
  if (!q) return null
  const qTokens = q.split(' ').filter((t) => t.length >= 2)
  if (qTokens.length === 0) return null

  let best = null
  let bestScore = 0
  for (const [key, entry] of _DICT_INDEX) {
    if (!key) continue
    let score = 0
    if (key === q) {
      score = 1
    } else if (key.includes(q)) {
      score = 0.8 + (q.length / key.length) * 0.19
    } else {
      const kTokens = key.split(' ').filter((t) => t.length >= 2)
      let matches = 0
      for (const qt of qTokens) {
        for (const kt of kTokens) {
          if (kt.includes(qt) || qt.includes(kt)) { matches++; break }
        }
      }
      score = (matches / qTokens.length) * 0.8
    }
    if (score > bestScore) {
      bestScore = score
      best = entry
    }
  }
  return bestScore >= threshold ? best : null
}

function translateExerciseToEnglish(name) {
  const e = findExerciseEntry(name)
  return e ? e.en : null
}

function translateExerciseToSpanish(name) {
  const e = findExerciseEntry(name)
  return e ? e.es : null
}

function getExerciseImageFromDictionary(name) {
  const e = findExerciseEntry(name) || findExerciseEntryFuzzy(name)
  return e ? e.image : null
}

